def run_cli(*args: str) -> str:
    env = os.environ.copy()
    env["RUST_LOG"] = "error"
    res = subprocess.run(
        [str(ROOT / "target" / "release" / "lab_02_secret_sharing"), *args],
        capture_output=True,
        text=True,
        check=True,
        env=env,
    )
    return res.stdout


