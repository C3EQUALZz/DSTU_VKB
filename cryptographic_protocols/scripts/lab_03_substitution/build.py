def run_cli(text: str) -> str:
    env = os.environ.copy()
    env["RUST_LOG"] = "error"
    env["NO_COLOR"] = "1"
    short = " ".join(text.split()[:100])
    res = subprocess.run(
        [str(BIN), "analyze", short],
        capture_output=True, text=True, check=False, env=env,
    )
    raw = res.stdout if res.returncode == 0 else f"(rc={res.returncode}) {res.stderr[:200]}"
    return _sanitize(raw)


def _sanitize(s: str) -> str:
    # Удалить ANSI escape и любые управляющие символы кроме \n.
    s = re.sub(r"\x1b\[[0-9;]*[A-Za-z]", "", s)
    s = "".join(c for c in s if c == "\n" or 0x20 <= ord(c) < 0x10000)
    return s


def read_source(rel: str) -> str:
    return (CRATE_SRC / rel).read_text(encoding="utf-8")


def parse_codes(text: str) -> list[int]:
    return [int(t.rstrip(",.;:!?-")) for t in text.split() if re.fullmatch(r"-?\d+[,.;:!?-]?", t)]


def freq_table_text(codes: list[int]) -> str:
    counter = Counter(codes)
    lines = ["Код | Частота"]
    for code, n in counter.most_common(25):
        lines.append(f"{code:>4} | {n}")
    if len(counter) > 25:
        lines.append(f"... (всего различных кодов: {len(counter)})")
    return "\n".join(lines)


def initial_mapping(codes: list[int]) -> dict[int, str]:
    """Жадная сопоставка: самый частый код ↔ самой частой букве и т.д."""
    counter = Counter(codes)
    sorted_codes = [c for c, _ in counter.most_common()]
    mapping = {}
    for code, (letter, _) in zip(sorted_codes, RU_LETTERS_BY_FREQ):
        mapping[code] = letter
    return mapping


def apply_mapping(codes: list[int], mapping: dict[int, str]) -> str:
    return "".join(mapping.get(c, "?") for c in codes)


