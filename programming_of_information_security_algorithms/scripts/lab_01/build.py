"""Отчёт по лаб. 1 — RSA с нуля."""

from __future__ import annotations

import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT / "scripts"))

from report_builder import (  # noqa: E402
    LabMeta,
    add_bullets,
    add_heading,
    add_image,
    add_image_parts,
    add_listing,
    add_page_break,
    add_para,
    add_qa,
    add_table_simple,
    add_title_page,
    make_doc,
    save,
)

SHOTS = ROOT / "docs" / "screenshots" / "lab_01"
SNIPS = ROOT / "docs" / "snippets" / "lab_01"


def main() -> None:
    meta = LabMeta(number=1, title="Реализация алгоритмов асимметричной криптографии на примере RSA")
    doc = make_doc()
    add_title_page(doc, meta)
    add_page_break(doc)

    add_heading(doc, "Тема и цель работы")
    add_para(
        doc,
        "Тема: алгоритм асимметричной криптографии RSA — генерация пары "
        "ключей произвольной длины (на учебных значениях — 1024 бит), "
        "запись ключей в файлы, шифрование и расшифрование произвольного "
        "текстового сообщения.",
    )
    add_para(
        doc,
        "Цель работы: освоить математические основы RSA — арифметику по "
        "модулю, тест Миллера–Рабина на простоту, расширенный алгоритм "
        "Евклида для нахождения обратного по модулю, быстрое возведение "
        "в степень по модулю; реализовать систему «с нуля», без "
        "использования готовых криптографических библиотек; убедиться, "
        "что генерация ключей, шифрование и расшифрование работают "
        "согласованно — расшифрованное сообщение совпадает с исходным.",
    )

    add_heading(doc, "Условие задания", level=2)
    add_bullets(doc, [
        "Реализовать генерацию открытого и закрытого ключа RSA и "
        "записать полученные ключи в файлы.",
        "Реализовать с помощью сгенерированных ключей шифрование "
        "произвольного текстового сообщения. Полученный результат "
        "вывести на экран и записать в файл.",
        "Прочитать из файлов зашифрованное сообщение, ключи, расшифровать "
        "данные и вывести расшифрованное сообщение на экран.",
    ])
    add_para(
        doc,
        "Дополнительное ограничение проекта: не использовать сторонние "
        "криптокрейты (rsa, num-bigint, num-prime) — длинная арифметика "
        "и алгоритмы RSA должны быть написаны самостоятельно.",
    )

    add_heading(doc, "Архитектура и алгоритмы")
    add_heading(doc, "Структура крейта (Clean Architecture)", level=2)
    add_listing(doc,
        "crates/lab_01_rsa/src/\n"
        "├── domain/\n"
        "│   ├── bigint.rs       BigUint + операции (add, sub, mul, divrem,\n"
        "│   │                   mod_pow, gcd, mod_inverse через расширенный Евклид)\n"
        "│   ├── primes.rs       Miller-Rabin + generate_prime(bits, rng)\n"
        "│   ├── rng.rs          OsRng (/dev/urandom) + DeterministicRng (для тестов)\n"
        "│   └── rsa.rs          KeyPair, PublicKey, PrivateKey, encrypt, decrypt\n"
        "├── infrastructure/\n"
        "│   └── storage.rs      Чтение/запись ключей и шифртекста в текстовом\n"
        "│                       формате (hex для bigint-значений)\n"
        "├── application/\n"
        "│   └── usecases.rs     GenerateKeysUseCase, EncryptUseCase, DecryptUseCase\n"
        "├── presentation/\n"
        "│   └── cli.rs          clap: gen | encrypt | decrypt\n"
        "└── tests/cli.rs        e2e gen → encrypt → decrypt через бинарь",
        caption="Листинг 1 — структура каталогов крейта"
    )

    add_heading(doc, "Тип KeyPair / PublicKey / PrivateKey", level=2)
    add_para(doc,
        "Открытый ключ — пара (n, e), где n — модуль (произведение двух "
        "больших простых p, q), e — фиксированная экспонента 65537. "
        "Закрытый ключ — кортеж (n, d, p, q), где d — обратное к e по "
        "модулю φ(n) = (p-1)(q-1). Все длинные числа представлены типом "
        "BigUint собственной реализации.")
    add_image_parts(doc, SNIPS, "01_keys_struct", "Листинг 2 — определение типов ключей RSA")

    add_heading(doc, "Тест простоты Миллера–Рабина", level=2)
    add_para(doc,
        "Случайные простые p и q генерируются последовательно: создаётся "
        "случайное число нужной разрядности, у которого установлены "
        "старший и младший биты (чтобы число было точно "
        "(bits)-битным и нечётным); сначала выполняется быстрый отсев "
        "делением на малые простые ≤ 317, затем — 20 раундов теста "
        "Миллера–Рабина со случайными свидетелями. Вероятность того, что "
        "составное число пройдёт 20 раундов — не более 4⁻²⁰.")
    add_image_parts(doc, SNIPS, "02_miller_rabin", "Листинг 3 — тест простоты Миллера–Рабина")
    add_image_parts(doc, SNIPS, "03_generate_prime", "Листинг 4 — генерация случайного простого нужной разрядности")

    add_heading(doc, "Шифрование и расшифрование", level=2)
    add_para(doc,
        "Шифрование — учебная схема без PKCS#1 padding: исходное сообщение "
        "разбивается на блоки по (n_bytes − 1) байт, каждый блок интерпретируется "
        "как BigUint m, и преобразуется в c = m^e mod n. Длина исходного "
        "сообщения записывается в заголовок шифртекста как u64 — это "
        "нужно, чтобы корректно восстановить хвостовой блок при дешифровке. "
        "Расшифрование — обратное преобразование m = c^d mod n.")
    add_image_parts(doc, SNIPS, "04_encrypt_decrypt", "Листинг 5 — функции encrypt / decrypt над BigUint")

    add_heading(doc, "CLI-фасад", level=2)
    add_image_parts(doc, SNIPS, "05_cli", "Листинг 6 — точка входа CLI (clap-команды)")

    add_page_break(doc)
    add_heading(doc, "Файловый формат ключей")
    add_para(doc,
        "Использован собственный текстовый формат — пары «ключ = значение», "
        "значения BigUint — в шестнадцатеричном виде с префиксом 0x. "
        "От PEM отказались, чтобы не подключать base64 и не маскировать "
        "учебный характер реализации.")
    add_listing(doc,
        "# RSA public key (учебный, лаб 1)\n"
        "bits = 1024\n"
        "n = 0x...\n"
        "e = 0x010001\n"
        "\n"
        "# RSA private key (учебный, лаб 1)\n"
        "bits = 1024\n"
        "n = 0x...\n"
        "e = 0x010001\n"
        "d = 0x...\n"
        "p = 0x...\n"
        "q = 0x...",
        caption="Листинг 7 — формат файлов с ключами"
    )

    add_heading(doc, "Сборка и запуск")
    add_listing(doc,
        "cargo build --workspace --release\n"
        "\n"
        "# 1) Генерация пары ключей\n"
        "just lab-01 gen --bits 1024 \\\n"
        "    --public  artifacts/lab_01/pub.key \\\n"
        "    --private artifacts/lab_01/priv.key\n"
        "\n"
        "# 2) Шифрование произвольного текста\n"
        "just lab-01 encrypt --public artifacts/lab_01/pub.key \\\n"
        "    --in  docs/explanations/lab_01_rsa/sample.txt \\\n"
        "    --out artifacts/lab_01/sample.enc\n"
        "\n"
        "# 3) Расшифрование (выводит на экран и сохраняет в файл)\n"
        "just lab-01 decrypt --private artifacts/lab_01/priv.key \\\n"
        "    --in  artifacts/lab_01/sample.enc \\\n"
        "    --out artifacts/lab_01/sample.dec",
        caption="Листинг 8 — команды запуска"
    )

    add_page_break(doc)
    add_heading(doc, "Результаты запуска")
    add_image_parts(doc, SHOTS, "01_gen_keys", "Рисунок 1 — генерация пары ключей RSA-1024")
    add_image_parts(doc, SHOTS, "02_encrypt", "Рисунок 2 — шифрование sample.txt (219 байт → 264 байта шифртекста)")
    add_image_parts(doc, SHOTS, "03_decrypt", "Рисунок 3 — расшифрование шифртекста и вывод исходного текста")
    add_image_parts(doc, SHOTS, "04_pub_key", "Рисунок 4 — фрагмент сформированного файла открытого ключа (head -20)")

    add_page_break(doc)
    add_heading(doc, "Контроль качества и тестирование")
    add_para(doc,
        "Тесты сгруппированы по слоям Clean Architecture. Все 42 теста "
        "проходят зелёным: cargo test, clippy и rustfmt дают чистый "
        "прогон.")
    add_table_simple(doc, [
        ["Уровень", "Файл", "Что проверяет"],
        ["Domain (unit)", "src/domain/bigint.rs", "Арифметика BigUint: +, −, ×, /, mod_pow, mod_inverse, gcd"],
        ["Domain (vectors)", "src/domain/rsa.rs", "Векторы маленьких параметров: p=61, q=53, n=3233, e=17, d=2753"],
        ["Domain (property)", "—", "decrypt(encrypt(m, pub), priv) = m на случайных m < n"],
        ["Domain (primes)", "src/domain/primes.rs", "Известные простые проходят Miller-Rabin; составные отбраковываются"],
        ["Infrastructure", "src/infrastructure/storage.rs", "Round-trip ключей и шифртекста через файлы"],
        ["Application", "src/application/usecases.rs", "Сценарии gen / encrypt / decrypt"],
        ["CLI (e2e)", "tests/cli.rs", "Полный цикл gen → encrypt → decrypt через бинарь; --help"],
        ["Итого", "—", "42 теста"],
    ], caption="Таблица 1 — покрытие тестами")

    add_page_break(doc)
    add_heading(doc, "Контрольные вопросы")
    QA = [
        ("На чём основана криптостойкость RSA?",
         "На сложности задачи факторизации больших целых чисел: имея "
         "только n = p·q, восстановить p и q вычислительно сложно для "
         "достаточно больших n (1024 бит и более). Закрытая экспонента d "
         "связана с p и q через φ(n) = (p-1)(q-1) и e·d ≡ 1 (mod φ(n)); "
         "без знания φ(n) восстановить d по e и n невозможно за разумное "
         "время. На квантовом компьютере с алгоритмом Шора эта задача "
         "решается за полиномиальное время — поэтому RSA не считается "
         "квантово-стойким."),
        ("Зачем нужен тест Миллера–Рабина и почему 20 раундов?",
         "Чтобы убедиться, что сгенерированное случайное нечётное число "
         "достаточно большой разрядности — простое, его нужно проверить. "
         "Детерминированный тест простоты (например, AKS) слишком "
         "медленный для 512-битных чисел; пробное деление неэффективно "
         "на этом масштабе. Тест Миллера–Рабина — вероятностный: каждый "
         "раунд с одним случайным свидетелем имеет вероятность ошибки "
         "≤ 1/4 для составных чисел. После k раундов вероятность "
         "пропустить составное ≤ 4⁻ᵏ. 20 раундов дают ≤ 4⁻²⁰ ≈ 10⁻¹² — "
         "ниже вероятности ошибок оборудования."),
        ("Почему e = 65537 — типичный выбор?",
         "65537 = 2¹⁶ + 1 — четвёртое простое число Ферма. Простота "
         "гарантирует, что gcd(e, φ(n)) = 1 при любом φ(n), не "
         "кратном 65537 (что практически никогда не случается). Малое "
         "число единичных бит (только 2) делает быстрое возведение в "
         "степень mod n очень эффективным: 16 удвоений + 1 умножение. "
         "Меньшие значения (типа e = 3) уязвимы для атак Кокса–Хастада, "
         "если одно и то же сообщение зашифровано на разных ключах с e=3."),
        ("Что такое блочный режим в реализации?",
         "RSA работает над одним числом m < n. Чтобы зашифровать "
         "произвольное текстовое сообщение, его нужно разбить на блоки "
         "размером менее n. В реализации блок имеет длину (n_bytes − 1) "
         "байт — это гарантирует, что блок всегда меньше n. Длина "
         "исходного сообщения записывается в шапку шифртекста как u64, "
         "чтобы при расшифровании корректно восстановить хвост."),
        ("Зачем хранить в закрытом ключе p и q отдельно от d?",
         "Это позволяет ускорить расшифрование через китайскую теорему "
         "об остатках (CRT): расшифровка по модулям p и q выполняется "
         "в 4 раза быстрее, чем расшифровка по модулю n. В учебной "
         "реализации мы не используем CRT (по простоте), но "
         "сохраняем p и q в файле, чтобы реализация была расширяемой. "
         "Также p и q нужны для проверки целостности ключа."),
        ("В чём недостаток учебной реализации RSA без PKCS#1 padding?",
         "Без padding RSA детерминирован — одно и то же сообщение, "
         "зашифрованное одним и тем же ключом, даёт одинаковый "
         "шифртекст, что нарушает семантическую стойкость. Атакующий "
         "может построить словарь популярных сообщений. Также «голый» "
         "RSA уязвим для атаки выбранного шифртекста (Bleichenbacher, "
         "1998 — на PKCS#1 v1.5, и более тонкие атаки на raw-RSA). В "
         "промышленных реализациях используют OAEP (RSA-OAEP-SHA256, как "
         "в лаб 7) или подпись через RSA-PSS."),
    ]
    for i, (q, a) in enumerate(QA, start=1):
        add_qa(doc, i, q, a)

    add_page_break(doc)
    add_heading(doc, "Выводы")
    add_para(doc,
        "В рамках лабораторной работы №1 с нуля реализована учебная "
        "схема RSA. Собственная длинная арифметика BigUint поддерживает "
        "все операции, необходимые для RSA: сложение, вычитание, "
        "умножение, деление с остатком, возведение в степень по модулю, "
        "наибольший общий делитель и обратное по модулю через "
        "расширенный алгоритм Евклида. Тест Миллера–Рабина с 20 "
        "раундами обеспечивает приемлемую для учебной работы "
        "вероятность ложного срабатывания.")
    add_para(doc,
        "Все три обязательных пункта условия выполнены: генерация ключей "
        "записывает открытый и закрытый ключи в текстовые файлы; "
        "шифрование произвольного текста печатает результат и "
        "сохраняет его в файл; расшифрование читает шифртекст и оба "
        "ключа из файлов, восстанавливает исходное сообщение и выводит "
        "его на экран. Программа покрыта 42 автоматическими тестами "
        "(включая полный цикл gen → encrypt → decrypt через бинарь); "
        "clippy с флагом -D warnings и cargo fmt дают чистый прогон.")

    out = ROOT / "docs" / "reports" / "lab_01" / "Ковалев Д.П. ВКБ43 1 лаба.docx"
    save(doc, out)
    print(f"saved: {out}")


if __name__ == "__main__":
    main()
