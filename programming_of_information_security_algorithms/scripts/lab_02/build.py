"""Отчёт по лаб. 2 — SHA-2-256 по FIPS 180-4 с нуля."""

from __future__ import annotations

import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT / "scripts"))

from report_builder import (  # noqa: E402
    LabMeta,
    add_bullets,
    add_heading,
    add_image,
    add_image_parts,
    add_listing,
    add_page_break,
    add_para,
    add_qa,
    add_table_simple,
    add_title_page,
    make_doc,
    save,
)

SHOTS = ROOT / "docs" / "screenshots" / "lab_02"
SNIPS = ROOT / "docs" / "snippets" / "lab_02"


def main() -> None:
    meta = LabMeta(number=2, title="Реализация алгоритма хеширования SHA-2-256")
    doc = make_doc()
    add_title_page(doc, meta)
    add_page_break(doc)

    add_heading(doc, "Тема и цель работы")
    add_para(doc,
        "Тема: криптографические хеш-функции и стандарт SHA-2 — расчёт "
        "значения хеш для произвольного файла с помощью SHA-2-256 (вариант "
        "256-битного выхода), запись хеша в отдельный файл и последующая "
        "проверка целостности файла путём пересчёта и сравнения с "
        "сохранённым значением.")
    add_para(doc,
        "Цель работы: освоить реализацию криптографической хеш-функции "
        "согласно стандарту FIPS 180-4: подготовка сообщения (padding до "
        "кратности 512 бит и добавление длины), инициализация 8 "
        "констант начального состояния, расширение блока на 64 слова через "
        "функции σ₀/σ₁ и подключение раундовой функции с использованием "
        "функций Ch, Maj, Σ₀, Σ₁ и 64 константных значений K. Условие "
        "запрещает использование сторонних библиотек хеширования.")

    add_heading(doc, "Условие задания", level=2)
    add_bullets(doc, [
        "Реализовать для произвольного текстового файла расчёт значения "
        "хеша на основе алгоритма SHA-2-256.",
        "Полученное значение хеша записать в отдельный файл.",
        "В реализации алгоритма не использовать сторонние библиотеки, "
        "выполняющие вычисление хеш-функций.",
        "Реализовать проверку на совпадение значения хеша для "
        "произвольного файла, рассчитанного в приложении и прочитанного "
        "из ранее сохранённого файла.",
    ])

    add_heading(doc, "Архитектура и алгоритм")
    add_heading(doc, "Структура крейта", level=2)
    add_listing(doc,
        "crates/lab_02_sha256/src/\n"
        "├── domain/\n"
        "│   └── sha256.rs       Sha256 (new/update/finalize), функции Ch, Maj,\n"
        "│                       Σ₀, Σ₁, σ₀, σ₁; 8 H₀, 64 K по FIPS 180-4\n"
        "├── application/\n"
        "│   └── usecases.rs     HashFile, VerifyHash (стримит блоками 64 КБ)\n"
        "├── infrastructure/\n"
        "│   └── hash_io.rs      Чтение/запись .sha256 в формате coreutils\n"
        "├── presentation/\n"
        "│   └── cli.rs          clap: hash | verify\n"
        "└── tests/cli.rs        e2e + большие файлы + сверка с shasum",
        caption="Листинг 1 — структура крейта"
    )

    add_heading(doc, "Константы и начальное состояние", level=2)
    add_para(doc,
        "Согласно FIPS 180-4, 8 начальных значений H₀ — это первые 32 бита "
        "дробной части квадратных корней первых 8 простых чисел; 64 "
        "константы K — первые 32 бита дробной части кубических корней "
        "первых 64 простых чисел. Эти значения «без люка» (nothing-up-my-"
        "sleeve numbers) исключают сомнение в существовании скрытого "
        "ослабления алгоритма.")
    add_image_parts(doc, SNIPS, "01_constants", "Листинг 2 — константы H₀ и K по FIPS 180-4")

    add_heading(doc, "Вспомогательные функции", level=2)
    add_para(doc,
        "Шесть битовых функций: Ch(x,y,z) = (x ∧ y) ⊕ (¬x ∧ z) — выбор "
        "битов; Maj(x,y,z) = (x ∧ y) ⊕ (x ∧ z) ⊕ (y ∧ z) — мажоритарная; "
        "Σ₀(x) = ROTR²(x) ⊕ ROTR¹³(x) ⊕ ROTR²²(x); Σ₁(x) = ROTR⁶(x) ⊕ "
        "ROTR¹¹(x) ⊕ ROTR²⁵(x); σ₀(x) = ROTR⁷(x) ⊕ ROTR¹⁸(x) ⊕ SHR³(x); "
        "σ₁(x) = ROTR¹⁷(x) ⊕ ROTR¹⁹(x) ⊕ SHR¹⁰(x). Реализованы как "
        "`const fn` — Rust-компилятор вычислит их в compile-time, где "
        "возможно.")
    add_image_parts(doc, SNIPS, "02_helpers", "Листинг 3 — битовые функции Ch, Maj, Σ₀, Σ₁, σ₀, σ₁")

    add_heading(doc, "Функция сжатия и инкрементальное хеширование", level=2)
    add_para(doc,
        "Sha256 хранит текущее состояние (H[0..7]), буфер недозаполненного "
        "блока и счётчик битов сообщения. На каждом полном 64-байтовом "
        "блоке вызывается функция сжатия: блок расширяется на 64 32-битных "
        "слова через σ₀ и σ₁, затем выполняется 64 раунда по схеме SHA-256 "
        "(каждый раунд обновляет 8 регистров a..h, используя K[i] и W[i]). "
        "После всех блоков формируется padding (0x80 || zeros || 64-bit "
        "длина в битах) и обрабатывается финальный блок.")
    add_image_parts(doc, SNIPS, "03_compress", "Листинг 4 — функция сжатия одного 64-байтового блока")

    add_heading(doc, "Точка входа digest и стриминг файла", level=2)
    add_para(doc,
        "Функция digest(data) — обёртка над инкрементальным API: создаёт "
        "Sha256::new(), вызывает update(data) и finalize(). При обработке "
        "файла приложение использует именно инкрементальный API: читает "
        "блоками по 64 КБ через std::io::BufReader и подаёт каждый "
        "буфер в update — это позволяет обрабатывать файлы любого "
        "размера без загрузки в память.")
    add_image_parts(doc, SNIPS, "04_digest_top", "Листинг 5 — функция digest и публичный API")

    add_heading(doc, "Формат файла .sha256", level=2)
    add_para(doc,
        "Файл хеша совместим с форматом coreutils sha256sum / shasum: "
        "одна строка вида «{hex} {имя_файла}», где hex — 64 строчных "
        "шестнадцатеричных символа. Совместимость позволяет проверить "
        "наш файл системной утилитой (`shasum -a 256 -c file.sha256`) — "
        "это дополнительная гарантия корректности.")
    add_image_parts(doc, SNIPS, "05_hash_io", "Листинг 6 — чтение и запись .sha256 (совместимо с coreutils)")

    add_page_break(doc)
    add_heading(doc, "Сборка и запуск")
    add_listing(doc,
        "cargo build --workspace --release\n"
        "\n"
        "# Расчёт хеша файла README.md\n"
        "just lab-02 hash README.md --out artifacts/lab_02/readme.sha256\n"
        "\n"
        "# Проверка — приложение читает .sha256 и сравнивает с\n"
        "# заново рассчитанным значением\n"
        "just lab-02 verify README.md --against artifacts/lab_02/readme.sha256\n"
        "\n"
        "# Внешняя сверка системной утилитой macOS\n"
        "shasum -a 256 README.md",
        caption="Листинг 7 — команды запуска"
    )

    add_heading(doc, "Результаты запуска")
    add_image_parts(doc, SHOTS, "01_hash", "Рисунок 1 — расчёт хеша README.md и запись в .sha256")
    add_image_parts(doc, SHOTS, "02_verify_ok", "Рисунок 2 — проверка: вердикт MATCH (хеши совпадают)")
    add_image_parts(doc, SHOTS, "03_shasum_compare", "Рисунок 3 — сверка с системным shasum -a 256 (значения побайтово совпадают)")

    add_page_break(doc)
    add_heading(doc, "Тестовые векторы NIST")
    add_para(doc,
        "Реализация проверена на известных тестовых векторах из FIPS "
        "180-4 §B.1, §B.2 и §B.3. Все четыре вектора дают побайтовое "
        "совпадение с эталонными значениями NIST и с системным shasum.")
    add_table_simple(doc, [
        ["Вход", "Длина", "Ожидаемый SHA-256"],
        ["\"\" (пусто)", "0 байт",
         "e3b0c44298fc1c149afbf4c8996fb924\n27ae41e4649b934ca495991b7852b855"],
        ["\"abc\"", "3 байта",
         "ba7816bf8f01cfea414140de5dae2223\nb00361a396177a9cb410ff61f20015ad"],
        ["FIPS 180-4 Annex B.2", "56 байт",
         "248d6a61d20638b8e5c026930c3e6039\na33ce45964ff2167f6ecedd419db06c1"],
        ["10⁶ × 'a'", "1 МБ",
         "cdc76e5c9914fb9281a1c7e284d73e67\nf1809a48a497200e046d39ccc7112cd0"],
    ], caption="Таблица 1 — тестовые векторы из FIPS 180-4 (совпадают с реализацией)")

    add_heading(doc, "Покрытие тестами")
    add_table_simple(doc, [
        ["Уровень", "Тестов", "Что проверяет"],
        ["Domain (NIST vectors)", "5", "Векторы FIPS 180-4 §B.1, §B.2; 64×'a'; 1М 'a' (single-shot и streaming)"],
        ["Domain (граница)", "2", "55 и 56 байт — граница padding на стыке блока"],
        ["Domain (hex round-trip)", "2", "to_hex/from_hex согласованы"],
        ["Property", "3", "streaming↔one-shot при случайном разбиении; длина 32 байта; hex round-trip"],
        ["Infrastructure", "3", "save/load .sha256 round-trip; совместимость с форматом coreutils; пустой файл → ошибка"],
        ["CLI (e2e)", "7", "Хеш `abc`; запись в shasum-формате; verify совпадение → exit 0; изменение файла → ≠ 0 + MISMATCH; парсинг coreutils-файла; 5 МиБ; --help"],
        ["Итого", "24 + 1 doctest", "—"],
    ], caption="Таблица 2 — покрытие автотестами")

    add_page_break(doc)
    add_heading(doc, "Контрольные вопросы")
    QA = [
        ("Почему SHA-2-256 считается криптографически стойкой?",
         "SHA-2-256 удовлетворяет трём свойствам: 1) preimage resistance — "
         "по хешу h практически невозможно найти m, такое что H(m)=h "
         "(сложность ≈ 2²⁵⁶); 2) second preimage resistance — для "
         "заданного m₁ невозможно найти m₂ ≠ m₁ с тем же хешем "
         "(сложность ≈ 2²⁵⁶); 3) collision resistance — невозможно найти "
         "любую пару m₁ ≠ m₂ с одинаковым хешем (сложность ≈ 2¹²⁸ из-за "
         "парадокса дней рождения). На SHA-2 не найдено практических "
         "атак, заметно превосходящих эти границы."),
        ("Что такое padding и зачем он нужен?",
         "Padding — дополнение исходного сообщения до длины, кратной "
         "размеру блока (для SHA-256 — 512 бит). Без padding нельзя "
         "обработать сообщение, длина которого не кратна 64 байтам. "
         "Padding по FIPS 180-4 строится так: к сообщению добавляется "
         "бит '1' (байт 0x80), затем нули — столько, чтобы общая длина "
         "(сообщение + 0x80 + нули + 8 байт длины) была кратна 64; в "
         "последние 8 байтов кодируется исходная длина в битах. Такая "
         "схема гарантирует, что разные сообщения не приведут к "
         "одинаковой паддированной последовательности."),
        ("Зачем нужны функции Σ₀, Σ₁, σ₀, σ₁?",
         "Эти функции — основной источник нелинейности и диффузии в "
         "SHA-2. Σ₀ и Σ₁ применяются в раундовой функции к "
         "состояниям a..h, σ₀ и σ₁ — в расширении блока с 16 слов до 64. "
         "Все четыре — комбинация трёх циклических сдвигов и побитового "
         "XOR; такая структура гарантирует, что изменение одного бита "
         "входа влияет на много бит выхода за несколько итераций "
         "(лавинный эффект)."),
        ("Чем .sha256-файл нашего приложения совместим с системным shasum?",
         "Использован формат coreutils: одна строка `{hex64} "
         " {filename}` — два пробела между значением и именем. "
         "Приложение читает оба варианта (`shasum -a 256 README.md > "
         "file.sha256` и наш собственный output) — оба корректно "
         "парсятся. Это позволяет преподавателю проверить целостность "
         "нашего хеша внешней утилитой: `shasum -a 256 -c "
         "readme.sha256` должно дать «OK»."),
        ("Почему в реализации запрещены сторонние библиотеки?",
         "Условие требует понимания внутренней структуры алгоритма. "
         "Использование готовой библиотеки sha2 или ring сводило бы "
         "лабораторную к одному вызову sha2::Sha256::digest(data) — это "
         "не давало бы ни понимания работы алгоритма, ни возможности "
         "разобрать padding, raund-функцию, расширение блока. Самостоятельная "
         "реализация по FIPS 180-4 — образовательная цель работы."),
    ]
    for i, (q, a) in enumerate(QA, start=1):
        add_qa(doc, i, q, a)

    add_page_break(doc)
    add_heading(doc, "Выводы")
    add_para(doc,
        "В рамках лабораторной работы №2 с нуля реализован алгоритм "
        "SHA-2-256 согласно стандарту FIPS 180-4: 8 начальных значений "
        "H₀, 64 константы K, шесть битовых функций (Ch, Maj, четыре Σ/σ), "
        "функция сжатия одного блока с 64 раундами, padding с записью "
        "длины и инкрементальный API (update/finalize). Сторонние "
        "хеш-библиотеки не использовались — алгоритм написан "
        "самостоятельно от объявления констант до финального дайджеста.")
    add_para(doc,
        "Корректность подтверждена двумя независимыми способами: "
        "1) собственным набором из 24 тестов, включающим четыре "
        "тестовых вектора из FIPS 180-4 и проверки на граничные размеры "
        "входа; 2) сверкой с системным shasum -a 256 на macOS — "
        "значения совпадают побайтово. Файл .sha256 совместим с "
        "форматом coreutils, что позволяет проверить целостность "
        "произвольного файла через `shasum -a 256 -c file.sha256`. "
        "Оба требуемых условием режима — расчёт хеша и проверка "
        "совпадения с ранее сохранённым — реализованы и продемонстрированы.")

    out = ROOT / "docs" / "reports" / "lab_02" / "Ковалев Д.П. ВКБ43 2 лаба.docx"
    save(doc, out)
    print(f"saved: {out}")


if __name__ == "__main__":
    main()
