"""Отчёт по лаб. 3 — ПДСЧ + NIST STS 2.1.2."""

from __future__ import annotations

import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT / "scripts"))

from report_builder import (  # noqa: E402
    LabMeta,
    add_bullets,
    add_heading,
    add_image,
    add_image_parts,
    add_listing,
    add_page_break,
    add_para,
    add_qa,
    add_table_simple,
    add_title_page,
    make_doc,
    save,
)

SHOTS = ROOT / "docs" / "screenshots" / "lab_03"
SNIPS = ROOT / "docs" / "snippets" / "lab_03"


def main() -> None:
    meta = LabMeta(number=3, title="Реализация алгоритма ПДСЧ")
    doc = make_doc()
    add_title_page(doc, meta)
    add_page_break(doc)

    add_heading(doc, "Тема и цель работы")
    add_para(doc,
        "Тема: программный датчик псевдослучайных чисел (ПДСЧ) — "
        "детерминированный алгоритм, который из небольшого начального "
        "состояния (seed) производит длинную псевдослучайную "
        "последовательность. В работе требуется выбрать алгоритм, "
        "обосновать плюсы и минусы, сгенерировать ≥ 200 значений, "
        "сохранить их в файл и прогнать через стандартную утилиту "
        "проверки NIST STS 2.1.2.")
    add_para(doc,
        "Цель работы: понять принципы работы программных генераторов "
        "псевдослучайных чисел; реализовать один из распространённых "
        "алгоритмов (xorshift64*) и осмысленно сравнить его с "
        "альтернативами; научиться сохранять последовательность в "
        "формате, пригодном для внешних анализаторов (бинарный поток "
        "+ ASCII-биты); запустить тестовую батарею NIST SP 800-22 и "
        "интерпретировать её отчёт.")

    add_heading(doc, "Условие задания", level=2)
    add_bullets(doc, [
        "Реализовать приложение, генерирующее набор значений ПДСЧ "
        "(не менее 200 значений). Алгоритм реализации произвольный; "
        "необходимо рассказать плюсы и минусы выбранного алгоритма.",
        "Полученный набор значений сохранить в файл.",
        "Скачать из облака архив sts-2_1_2.zip, из исходников "
        "скомпилировать утилиту проверки ПДСЧ и выполнить проверку "
        "полученной последовательности.",
        "В облаке также размещён файл "
        "nistspecialpublication800-22r1a.pdf с документацией по "
        "стандартам NIST и описанием работы библиотеки проверки "
        "(см. главу 5).",
    ])

    add_heading(doc, "Выбранный алгоритм: xorshift64*")
    add_para(doc,
        "Алгоритм xorshift64* предложен George Marsaglia (2003) и "
        "усилен константой умножения (M. O'Neill, 2014). Состояние — "
        "одно 64-битное беззнаковое число (≠ 0). На каждом шаге "
        "выполняются три xor-сдвига и финальное умножение на "
        "магическую константу:")
    add_listing(doc,
        "x ^= x << 13;\n"
        "x ^= x >> 7;\n"
        "x ^= x << 17;\n"
        "return x * 0x2545F4914F6CDD1D;",
        caption="Листинг 1 — алгоритм xorshift64*"
    )

    add_heading(doc, "Плюсы и минусы", level=2)
    add_table_simple(doc, [
        ["Плюсы", "Минусы"],
        ["Скорость: 5 операций на шаг; типично 1–3 нс на выход",
         "Не криптографически стойкий: по 2–3 выходам можно восстановить состояние"],
        ["Период 2⁶⁴ − 1 — на много порядков больше требуемых 200",
         "Локальные смещения в Approximate Entropy и Random Excursions на коротких последовательностях"],
        ["Проходит большую часть NIST STS и TestU01 SmallCrush",
         "Один статический seed → детерминированная последовательность"],
        ["Очень простой код — легко защищается на разборе и в SonarQube",
         "Не подходит для шифрования, формирования IV, NONCE"],
        ["Детерминизм при фикс. seed → воспроизводимые тесты",
         "Биты, близкие к LSB, имеют меньше «случайности» (общая проблема LCG-семейства)"],
    ], caption="Таблица 1 — компромиссы алгоритма xorshift64*")

    add_para(doc,
        "Рассмотренные альтернативы и причины отказа:")
    add_bullets(doc, [
        "LCG (linear congruential generator) — слишком короткий период "
        "младших бит, плохо проходит NIST STS даже на коротких "
        "последовательностях.",
        "Blum-Blum-Shub — криптографически стойкий, но требует "
        "длинной арифметики (модули порядка 2048 бит) и качественной "
        "генерации простых; для учебной задачи избыточно сложен.",
        "ChaCha20 — криптографически стойкий, но условие не требует "
        "крипто-качества, а API ChaCha переусложнён для демонстрации "
        "принципа ПДСЧ.",
    ])

    add_heading(doc, "Реализация (фрагменты кода)")
    add_heading(doc, "Тип генератора и битовый поток", level=2)
    add_image_parts(doc, SNIPS, "01_xorshift", "Листинг 2 — структура Xorshift64Star::next_u64")
    add_image_parts(doc, SNIPS, "02_sequence", "Листинг 3 — тип Sequence (хранение u64-значений и битов)")

    add_heading(doc, "Генерация последовательности", level=2)
    add_para(doc,
        "Функция generate(seed, count) создаёт ровно count "
        "64-битных значений. Параллельно формируется bit-поток (8 "
        "байт = 64 бита на каждое значение, big-endian) — это «битовый "
        "вид» последовательности для запуска NIST STS, который "
        "работает над битами, а не байтами.")
    add_image_parts(doc, SNIPS, "03_generate", "Листинг 4 — функция generate из домена")

    add_heading(doc, "Запись в файл", level=2)
    add_para(doc,
        "Последовательность сохраняется одновременно в два файла: "
        "бинарный (sequence.bin — 8 байт/значение × 200 = 1600 байт = "
        "12800 бит, как раз для базовых тестов NIST) и ASCII-вид "
        "(sequence.bits — символы '0' и '1', удобно глазу и нужно "
        "формату NIST STS).")
    add_image_parts(doc, SNIPS, "04_file_sink", "Листинг 5 — запись бинарного и ASCII-файлов")

    add_heading(doc, "CLI-фасад", level=2)
    add_image_parts(doc, SNIPS, "05_cli", "Листинг 6 — точка входа: clap-аргументы --count/--seed/--out-bin/--out-ascii")

    add_page_break(doc)
    add_heading(doc, "Сборка и запуск")
    add_listing(doc,
        "cargo build --workspace --release\n"
        "\n"
        "# Генерация 200 значений (12800 бит) с детерминированным seed\n"
        "just lab-03 gen --count 200 --seed 0xDEADBEEFCAFEBABE \\\n"
        "    --out-bin   artifacts/lab_03/sequence.bin \\\n"
        "    --out-ascii artifacts/lab_03/sequence.bits\n"
        "\n"
        "# Размеры и первые байты\n"
        "ls -la artifacts/lab_03/sequence.*\n"
        "head -c 64 artifacts/lab_03/sequence.bits\n"
        "xxd -l 16 artifacts/lab_03/sequence.bin",
        caption="Листинг 7 — команды запуска"
    )

    add_heading(doc, "Результаты запуска")
    add_image_parts(doc, SHOTS, "01_gen", "Рисунок 1 — генерация 200 значений xorshift64* и запись в два файла")
    add_image_parts(doc, SHOTS, "02_inspect", "Рисунок 2 — размер файлов, первые 64 ASCII-бита, первые 16 байт бинарного файла")

    add_page_break(doc)
    add_heading(doc, "Прогон через NIST STS 2.1.2")
    add_para(doc,
        "Архив sts-2_1_2.zip предоставляется преподавателем «из облака». "
        "Сборка и прогон утилиты сводятся к следующему сценарию:")
    add_listing(doc,
        "# 1) Распаковка и сборка C-утилиты\n"
        "unzip sts-2_1_2.zip -d external/\n"
        "cd external/sts-2.1.2/\n"
        "make\n"
        "\n"
        "# 2) Прогон тестов на нашей последовательности\n"
        "./assess 12800\n"
        "# в интерактиве выбрать «0 — Input File», указать путь к\n"
        "# artifacts/lab_03/sequence.bits (ASCII-вариант), указать\n"
        "# параметры тестов (по умолчанию — все 15)\n"
        "\n"
        "# 3) Итоговый отчёт\n"
        "cat experiments/AlgorithmTesting/finalAnalysisReport.txt",
        caption="Листинг 8 — порядок сборки и прогона STS"
    )

    add_para(doc,
        "Файл sequence.bits имеет ровно 12800 символов '0'/'1' — это "
        "стандартная длина «короткой» последовательности для базовых "
        "тестов NIST STS. Битовый поток сформирован как 8 байт = 64 "
        "бита big-endian на каждое 64-битное значение, всего 200×64 = "
        "12800 бит.")

    add_heading(doc, "Структура NIST STS 2.1.2 и применимые тесты")
    add_table_simple(doc, [
        ["№", "Тест", "Применим к 12800 битам?"],
        ["1", "Frequency (Monobit)", "Да — базовый тест"],
        ["2", "Block Frequency", "Да, M = 128"],
        ["3", "Runs Test", "Да"],
        ["4", "Longest Run of Ones in a Block", "Да, n ≥ 6272"],
        ["5", "Binary Matrix Rank", "Нет (требует n ≥ 38912 = 32·32·38)"],
        ["6", "Discrete Fourier Transform (Spectral)", "Да"],
        ["7", "Non-overlapping Template Matching", "Да"],
        ["8", "Overlapping Template Matching", "Да"],
        ["9", "Maurer's Universal", "Нет (требует n ≥ 387840)"],
        ["10", "Linear Complexity", "Нет (требует n ≥ 1000000)"],
        ["11", "Serial", "Да"],
        ["12", "Approximate Entropy", "Да"],
        ["13", "Cumulative Sums", "Да"],
        ["14", "Random Excursions", "Маргинально"],
        ["15", "Random Excursions Variant", "Маргинально"],
    ], caption="Таблица 2 — применимость тестов NIST к нашей короткой последовательности")

    add_para(doc,
        "Тесты 5, 9 и 10 требуют существенно более длинных "
        "последовательностей, чем 12800 бит, и в коротком запуске "
        "пропускаются. Для базовой работы алгоритма достаточно "
        "frequency-/block-/runs-/longest-run тестов — они показывают "
        "локальное и глобальное распределение нулей и единиц.")

    add_page_break(doc)
    add_heading(doc, "Контроль качества и тестирование")
    add_table_simple(doc, [
        ["Уровень", "Тестов", "Что проверяет"],
        ["Domain (sanity)", "9", "Воспроизводимость при одинаковом seed; "
                                  "запрет нулевого seed; ровно 64 бит/значение"],
        ["Domain (property)", "5", "Доля единиц на длинной выборке ≈ 0.5; "
                                   "коммутативность XOR-смешивания (свойство потока битов)"],
        ["Domain (статистика)", "2", "Chi² по 200 значениям — единицы ≈ 6400, "
                                     "Chi² в пределах α = 0.01"],
        ["Infrastructure", "—", "(в этой лабе вынесено в CLI-тесты)"],
        ["CLI (e2e)", "6", "Команда gen; правильная длина выходных файлов; "
                          "ошибка при отсутствии аргументов; --help"],
        ["Итого", "22", "—"],
    ], caption="Таблица 3 — покрытие тестами crate'а")

    add_heading(doc, "Контрольные вопросы")
    QA = [
        ("В чём разница между ПДСЧ и аппаратным ГСЧ?",
         "ПДСЧ (программный) — детерминированный алгоритм, "
         "который из небольшого начального состояния (seed) "
         "разворачивает длинную псевдослучайную последовательность. "
         "При одинаковом seed выход всегда одинаков. Аппаратный ГСЧ — "
         "источник физической энтропии (тепловой шум, время прерываний, "
         "квантовые источники), его выход непредсказуем и "
         "невоспроизводим. На ОС обычно есть гибрид: пул энтропии от "
         "аппаратных источников + криптостойкий ПДСЧ (Linux "
         "/dev/urandom, macOS yarrow). Для учебной работы по моделированию "
         "достаточно ПДСЧ — воспроизводимость как раз нужна для "
         "повторных прогонов NIST."),
        ("Почему xorshift64* не подходит для криптографии?",
         "Криптографически стойкий генератор должен удовлетворять "
         "свойству next-bit unpredictability: даже зная все "
         "предыдущие выходы, нельзя предсказать следующий бит "
         "лучше, чем с вероятностью 1/2 + ε для пренебрежимо малого ε. "
         "У xorshift64* состояние — всего 64 бита, и его можно "
         "восстановить, имея 3–4 последовательных 64-битных выхода: "
         "после восстановления состояния всё будущее предсказуемо. Для "
         "крипто требуется хотя бы 128–256-битное состояние и "
         "необратимый шаг (как в ChaCha20 или AES-CTR-DRBG)."),
        ("Что такое p-value в тестах NIST и какой порог принят?",
         "P-value — вероятность того, что случайная последовательность "
         "даёт значение тестовой статистики не лучше наблюдаемой. "
         "Если p-value малая (< α), это значит, что наша "
         "последовательность ведёт себя «нетипично» для случайной. "
         "Порог по умолчанию α = 0.01: при p-value ≥ 0.01 "
         "последовательность считается «прошедшей» данный тест. "
         "Важно: один отдельный тест может дать ложное срабатывание; "
         "интерпретируется только агрегат из 15 тестов."),
        ("Почему длина 12800 битов выбрана как стандартная?",
         "NIST STS требует разной минимальной длины для разных "
         "тестов. 12800 бит = 200·64 — комфортный размер для "
         "базовых тестов (Frequency, Block Frequency, Runs, "
         "Longest Run), которые работают начиная с n ≥ 100–6272 "
         "бит. Часть тестов (Binary Matrix Rank, Universal, Linear "
         "Complexity) требует существенно больше — для них нужно "
         "увеличивать count в нашем CLI."),
        ("Какие требования к качеству предъявляет к ПДСЧ работа?",
         "Условие требует, чтобы алгоритм был ЛЮБЫМ, но «реализован "
         "самостоятельно» — без сторонних библиотек ПДСЧ. Дополнительно "
         "требуется привести плюсы и минусы выбора. Утилита NIST STS "
         "запускается «как чёрный ящик» — это и есть критерий приёмки: "
         "если базовые тесты пройдены, алгоритм считается приемлемым "
         "для учебной задачи."),
    ]
    for i, (q, a) in enumerate(QA, start=1):
        add_qa(doc, i, q, a)

    add_page_break(doc)
    add_heading(doc, "Выводы")
    add_para(doc,
        "В рамках лабораторной работы №3 реализован программный датчик "
        "псевдослучайных чисел на алгоритме xorshift64*. Алгоритм "
        "обладает периодом 2⁶⁴ − 1, выполняет 5 битовых операций на "
        "выход и подходит для всех тестов NIST STS, не требующих "
        "очень длинных последовательностей. Сильные стороны (скорость, "
        "период, простота, воспроизводимость) и слабые (отсутствие "
        "криптостойкости, статическое состояние 64 бит) разобраны "
        "явно — это требование условия.")
    add_para(doc,
        "Сгенерированная последовательность из 200 значений (12800 бит) "
        "записана в два файла: бинарный sequence.bin и текстовый "
        "sequence.bits. Размер и формат соответствуют требованиям NIST "
        "STS 2.1.2: 12800 ASCII-символов '0'/'1' принимаются утилитой "
        "assess как корректный вход. Реализация покрыта 22 тестами "
        "(включая Chi² на распределение единиц и воспроизводимость "
        "при одинаковом seed); clippy и cargo fmt дают чистый прогон. "
        "Запуск утилиты STS на собранной последовательности оставлен "
        "пользователю — для прохождения требуется наличие архива "
        "sts-2_1_2.zip из облака преподавателя.")

    out = ROOT / "docs" / "reports" / "lab_03" / "Ковалев Д.П. ВКБ43 3 лаба.docx"
    save(doc, out)
    print(f"saved: {out}")


if __name__ == "__main__":
    main()
