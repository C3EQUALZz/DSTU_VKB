"""Отчёт по лаб. 4 — собственная реализация одного теста NIST SP 800-22."""

from __future__ import annotations

import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT / "scripts"))

from report_builder import (  # noqa: E402
    LabMeta,
    add_bullets,
    add_heading,
    add_image,
    add_image_parts,
    add_listing,
    add_math,
    add_page_break,
    add_para,
    add_qa,
    add_table_simple,
    add_title_page,
    make_doc,
    save,
)

SHOTS = ROOT / "docs" / "screenshots" / "lab_04"
SNIPS = ROOT / "docs" / "snippets" / "lab_04"


def main() -> None:
    meta = LabMeta(number=4, title="Контроль качества выходной последовательности ПДСЧ — Monobit Frequency Test (NIST SP 800-22)")
    doc = make_doc()
    add_title_page(doc, meta)
    add_page_break(doc)

    add_heading(doc, "Тема и цель работы")
    add_para(doc,
        "Тема: самостоятельная реализация одного из тестов NIST SP "
        "800-22 для проверки качества псевдослучайной последовательности "
        "битов. Из 15 тестов выбран базовый и самый показательный — "
        "Frequency (Monobit) Test (§2.1) — оценка распределения нулей "
        "и единиц в последовательности; дополнительно реализован Runs "
        "Test (§2.3) для проверки числа переходов 0↔1.")
    add_para(doc,
        "Цель работы: применить математику NIST SP 800-22 §2.1 — "
        "сведение последовательности к сумме S_n = Σ (2bᵢ − 1), "
        "нормировка s_obs = |S_n|/√n и вычисление p-value через "
        "комплементарную функцию ошибок erfc(s_obs/√2); "
        "сравнить p-value с порогом α = 0.01 и сформировать вердикт "
        "RANDOM / NON-RANDOM; реализовать всё без использования libm "
        "(erfc написан через аппроксимацию Abramowitz-Stegun 7.1.26).")

    add_heading(doc, "Условие задания", level=2)
    add_bullets(doc, [
        "Из стандарта NIST выбрать одну из проверок на выбор и "
        "самостоятельно её реализовать.",
        "С помощью реализованного алгоритма проверить полученную ранее "
        "в лабораторной работе №3 последовательность.",
        "Результат проверки сохранить в файл.",
    ])

    add_heading(doc, "Математика теста Monobit Frequency (§2.1 NIST)")
    add_para(doc,
        "Пусть ε = ε₁ ε₂ … ε_n — последовательность из n битов "
        "(0 или 1). Вычисляем:",
    )
    add_listing(doc,
        "X_i = 2·ε_i − 1    (0 → −1, 1 → +1)\n"
        "S_n = X_1 + X_2 + … + X_n   (сумма по преобразованным битам)\n"
        "s_obs = |S_n| / √n     (нормировка)\n"
        "P-value = erfc(s_obs / √2)\n"
        "Verdict = RANDOM, если P-value ≥ α (α = 0.01)\n"
        "          NON-RANDOM иначе",
        caption="Листинг 1 — формулы теста Monobit Frequency"
    )
    add_para(doc,
        "Интуиция: если последовательность случайна, S_n должна быть "
        "близка к нулю (равное число +1 и −1). Большое по абсолютной "
        "величине S_n — повод усомниться в случайности. "
        "Функция erfc(·) даёт вероятность того, что нормированный sample "
        "случайного процесса даст значение не меньше |s_obs|.")

    add_heading(doc, "Реализация (фрагменты кода)")
    add_heading(doc, "Тест Monobit Frequency", level=2)
    add_image_parts(doc, SNIPS, "01_monobit", "Листинг 2 — функция monobit (домен)")

    add_heading(doc, "Дополнительный тест: Runs Test", level=2)
    add_para(doc,
        "Дополнительно реализован тест §2.3 — Runs Test. Он смотрит на "
        "количество переходов 0↔1 в последовательности и сравнивает "
        "это число с ожидаемым для случайной последовательности. "
        "Помогает поймать гипотезы вида «слишком много или слишком "
        "мало переключений».")
    add_image_parts(doc, SNIPS, "02_runs", "Листинг 3 — функция runs (домен)")

    add_heading(doc, "Реализация erfc без libm", level=2)
    add_para(doc,
        "Комплементарная функция ошибок реализована через аппроксимацию "
        "Abramowitz-Stegun 7.1.26 — полином 6-й степени в "
        "вспомогательной переменной t = 1/(1 + p·x). Погрешность ≤ "
        "1.5·10⁻⁷, что более чем достаточно для теста с порогом α = "
        "0.01. erfc(0) = 1, erfc(+∞) → 0; для отрицательных x "
        "используется симметрия erfc(−x) = 2 − erfc(x).")
    add_image_parts(doc, SNIPS, "03_erfc", "Листинг 4 — собственная реализация erfc")

    add_heading(doc, "Загрузчик последовательности из файла", level=2)
    add_para(doc,
        "Поддерживается два формата: ASCII-файл с символами '0' и '1' "
        "(такой формирует лаб 3 в sequence.bits) и бинарный файл, где "
        "биты упакованы по 8 в байт (sequence.bin). Загрузчик "
        "автодетектирует формат по содержимому.")
    add_image_parts(doc, SNIPS, "04_loader", "Листинг 5 — загрузка ASCII/бинарной последовательности")

    add_heading(doc, "Запись отчёта в файл", level=2)
    add_para(doc,
        "Условие требует «результат проверки сохранить в файл». Отчёт "
        "формируется в текстовом виде с подробным выводом: длина n, "
        "число единиц и нулей, S_n, s_obs, P-value, вердикт. Такой "
        "формат удобно читать глазами и легко парсить.")
    add_image_parts(doc, SNIPS, "05_report", "Листинг 6 — запись отчёта в файл report.txt")

    add_page_break(doc)
    add_heading(doc, "Сборка и запуск")
    add_listing(doc,
        "cargo build --workspace --release\n"
        "\n"
        "# Прогон Monobit + Runs на последовательности из лаб 3\n"
        "just lab-04 check \\\n"
        "    --input artifacts/lab_03/sequence.bits \\\n"
        "    --out   artifacts/lab_04/report.txt\n"
        "\n"
        "# Просмотр отчёта\n"
        "cat artifacts/lab_04/report.txt",
        caption="Листинг 7 — команды запуска"
    )

    add_heading(doc, "Результаты запуска")
    add_image_parts(doc, SHOTS, "01_check", "Рисунок 1 — прогон теста на последовательности из лаб 3 (12800 бит)")
    add_image_parts(doc, SHOTS, "02_report", "Рисунок 2 — содержимое сохранённого отчёта report.txt")

    add_page_break(doc)
    add_heading(doc, "Тестовые векторы")
    add_para(doc,
        "Реализация проверена на эталонных примерах из самого стандарта "
        "NIST SP 800-22 (§2.1.8 и §2.3.8 — контрольный пример "
        "ε = «1011010101» (n=10) и первые 100 бит десятичной записи π).")
    add_table_simple(doc, [
        ["Вход (Monobit)", "Ожидаемый P-value", "Наш результат"],
        ["ε = 1011010101 (n=10)", "0.527089", "0.527089 — совпадает"],
        ["100 бит π", "0.109599", "0.109599 — совпадает"],
        ["0¹⁰⁰⁰ (все нули)", "≈ 0", "0.000000 — Verdict: NON-RANDOM"],
        ["(01)⁵⁰⁰ — чередование", "≈ 1", "1.000000 — Verdict: RANDOM"],
    ], caption="Таблица 1 — тестовые векторы Monobit Frequency")

    add_heading(doc, "Покрытие тестами")
    add_table_simple(doc, [
        ["Уровень", "Тестов", "Что проверяет"],
        ["Domain (NIST vectors)", "5", "Контрольные значения §2.1.8 и §2.3.8 NIST"],
        ["Domain (erfc)", "4", "erfc(0)=1, erfc(+∞)≈0, симметрия erfc(−x)=2−erfc(x), приближение Abramowitz-Stegun"],
        ["Domain (edge)", "4", "Все нули → NON-RANDOM; все единицы → NON-RANDOM; чередование → high P-value для Monobit, low для Runs"],
        ["Property", "3", "Симметрия perm(ε) ⇒ тот же результат; добавление 1 бита изменяет результат на малую величину"],
        ["Infrastructure", "2", "Round-trip отчёта; парсинг ASCII и бинарного файлов"],
        ["CLI (e2e)", "6", "Команда check; ошибка при пустом входе; неверный формат → exit ≠ 0; --help"],
        ["Итого", "24", "—"],
    ], caption="Таблица 2 — покрытие тестами crate'а")

    add_page_break(doc)
    add_heading(doc, "Связь с лаб 3 (xorshift64*)")
    add_para(doc,
        "Прогон теста Monobit Frequency на нашей последовательности "
        "(12800 бит, seed 0xDEADBEEFCAFEBABE) даёт P-value > 0.01 — "
        "то есть нулевая гипотеза «последовательность случайна» не "
        "отвергается. Это означает: распределение единиц и нулей в "
        "xorshift64* достаточно близко к 50/50, чтобы пройти базовый "
        "тест NIST на длине 12800 бит. Полная батарея 15 тестов "
        "запускается отдельно через утилиту STS 2.1.2 (см. отчёт по "
        "лаб 3).")

    add_heading(doc, "Контрольные вопросы")
    QA = [
        ("Что такое P-value в статистических тестах?",
         "P-value — это вероятность того, что случайная "
         "последовательность даёт значение тестовой статистики не "
         "лучше наблюдаемой. Если P-value мала, значит, наша "
         "последовательность ведёт себя «нетипично» для случайной "
         "(нулевая гипотеза отвергается). Если P-value велика — нет "
         "оснований сомневаться в случайности. Порог α задаётся "
         "заранее (NIST по умолчанию α=0.01) и не зависит от теста."),
        ("Почему именно Monobit Frequency выбран в работе?",
         "Это самый простой и наглядный тест из 15 в NIST SP 800-22: "
         "одна формула, один полином (erfc), понятный смысл. "
         "Реализовать его за 30 строк кода и понять, что и почему он "
         "считает, — реалистично в рамках одной лабораторной. Более "
         "сложные тесты (Maurer's Universal, Linear Complexity) "
         "требуют существенно больше кода и более длинных входов, "
         "что выходит за рамки задания «выбрать одну проверку»."),
        ("Что делает функция erfc и почему она написана вручную?",
         "erfc(x) — комплементарная функция ошибок, равная 1 − erf(x), "
         "где erf — интеграл Гаусса. В NIST §2.1 формула P-value = "
         "erfc(s_obs/√2) — это в чистом виде интеграл от нормального "
         "распределения от |s_obs| до бесконечности. Мы реализовали "
         "erfc через полиномиальную аппроксимацию Abramowitz-Stegun, а "
         "не через libm::erfc, чтобы не зависеть от системной "
         "математической библиотеки и продемонстрировать, что NIST-"
         "тест полностью самодостаточен."),
        ("В чём разница между Monobit Frequency и Runs Test?",
         "Monobit смотрит на агрегированную сумму «+1/−1» — он "
         "почувствует перекос в долю единиц, но не заметит «слипания» "
         "(например, последовательность 1¹⁰⁰⁰⁰⁰⁰0¹⁰⁰⁰⁰⁰⁰ имеет идеальные "
         "50/50, но «слишком мало» переходов). Runs Test, наоборот, "
         "проверяет именно частоту переходов 0↔1: для случайной "
         "последовательности ожидаемое число серий — около n/2. "
         "Два теста дополнительны: вместе они закрывают и проблему "
         "перекоса, и проблему «склеивания»."),
        ("Что значит «результат прохождения» теста?",
         "В NIST SP 800-22 каждая последовательность даёт P-value на "
         "каждом тесте. Если P-value ≥ α — последовательность "
         "«проходит» этот тест (хотя одно прохождение не значит "
         "«гарантированно случайна» — это просто отсутствие "
         "противоречий). Для надёжной оценки на одном генераторе "
         "запускают M = 100–1000 независимых последовательностей "
         "и применяют второй уровень анализа — Chi² по распределению "
         "P-values (должно быть приблизительно равномерное на [0,1])."),
    ]
    for i, (q, a) in enumerate(QA, start=1):
        add_qa(doc, i, q, a)

    add_page_break(doc)
    add_heading(doc, "Выводы")
    add_para(doc,
        "В рамках лабораторной работы №4 самостоятельно реализован тест "
        "Monobit Frequency из стандарта NIST SP 800-22 §2.1. Вспомогательно "
        "реализован Runs Test §2.3 и собственная аппроксимация функции "
        "erfc (Abramowitz-Stegun 7.1.26) — то есть проект "
        "самодостаточен и не подключает libm или другие математические "
        "библиотеки. Алгоритм работает над битовым массивом, "
        "поддерживает оба формата вывода лаб 3 (ASCII '0'/'1' и "
        "упакованный бинарный поток).")
    add_para(doc,
        "Прогон теста на последовательности 12800 бит, полученной в "
        "лаб 3, даёт вердикт RANDOM (P-value > 0.01) — наша реализация "
        "xorshift64* успешно проходит базовый тест распределения нулей "
        "и единиц. Результаты проверки сохраняются в текстовый файл "
        "artifacts/lab_04/report.txt — это удовлетворяет требованию "
        "условия «результат проверки сохранить в файл». Реализация "
        "покрыта 24 автоматическими тестами, в том числе на контрольных "
        "векторах самого стандарта NIST; clippy с флагом -D warnings и "
        "cargo fmt проходят чистым прогоном.")

    out = ROOT / "docs" / "reports" / "lab_04" / "Ковалев Д.П. ВКБ43 4 лаба.docx"
    save(doc, out)
    print(f"saved: {out}")


if __name__ == "__main__":
    main()
