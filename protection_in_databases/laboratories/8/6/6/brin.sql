-- Создание таблицы
CREATE EXTENSION IF NOT EXISTS pg_trgm;
DROP TABLE IF EXISTS employees CASCADE;
DROP TABLE IF EXISTS departments CASCADE;
DROP INDEX IF EXISTS idx_departments_name;

CREATE TABLE employees
(
    employee_id SERIAL PRIMARY KEY,
    name        VARCHAR(100) UNIQUE               NOT NULL,
    salary      DECIMAL(10, 2) CHECK (salary > 0) NOT NULL
);

CREATE TABLE departments
(
    department_id   INTEGER PRIMARY KEY,
    department_name VARCHAR(100) UNIQUE NOT NULL,
    location        VARCHAR(100)        NOT NULL,
    FOREIGN KEY (department_id) REFERENCES employees(employee_id)
);

-- Создание индекса
CREATE INDEX idx_departments_name ON departments USING BRIN(department_name);

