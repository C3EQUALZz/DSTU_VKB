"""
Вариант 15. Написать и протестировать функцию, которая по заданной строке Str, содержащей буквы и цифры, формирует новую,
состоящую только из цифр, входящих в Str.
"""


def extract_digits(s: str) -> str:
    """
    Формирует новую строку, состоящую только из цифр, входящих в исходную строку.

    Аргументы:
        s (str): Исходная строка, содержащая буквы и/или цифры.

    Возвращает:
        str: Строка, состоящая только из цифр.
    """
    return ''.join(c for c in s if c.isdigit())


def main() -> None:
    """
    Основная функция, запрашивающая ввод у пользователя и выводящая результат.
    """
    user_input: str = input("Введите строку, содержащую буквы и цифры: ")
    result: str = extract_digits(user_input)
    print("Строка только с цифрами:", result)


if __name__ == "__main__":
    main()
