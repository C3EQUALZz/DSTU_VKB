"""
Описать функцию minmax(x,y), которая присваивает первому параметру большее, а второму - меньшее из значений х и у.
Используя эту функцию, перераспределить введенные значения переменных А, В, С так, чтобы стало А < В < С.
"""
from typing import Tuple


def minmax(x: float, y: float) -> Tuple[float, float]:
    """
    Возвращает кортеж, где первый элемент — большее из x и y,
    второй элемент — меньшее из x и y.

    Аргументы:
        x (float): Первое число.
        y (float): Второе число.

    Возвращает:
        Tuple[float, float]: (max(x, y), min(x, y))
    """
    return (x, y) if x > y else (y, x)


def main() -> None:
    """
    Основная функция программы.
    Получает ввод от пользователя, перераспределяет значения A, B, C
    так, чтобы стало A < B < C, и выводит результат.
    """
    try:
        a = float(input("Введите значение A: "))
        b = float(input("Введите значение B: "))
        c = float(input("Введите значение C: "))

        # Шаг 1: Упорядочить A и B
        a, b = minmax(a, b)

        # Шаг 2: Упорядочить A и C
        a, c = minmax(a, c)

        # Шаг 3: Упорядочить B и C
        b, c = minmax(b, c)

        # Шаг 4: Поменять местами A и C для получения A < B < C
        c, a = minmax(c, a)

        print(f"Результат: A = {a}, B = {b}, C = {c}")
        print(f"Проверка: {a < b < c}")

    except ValueError:
        print("Ошибка: Введите корректные числовые значения.")


if __name__ == "__main__":
    main()
