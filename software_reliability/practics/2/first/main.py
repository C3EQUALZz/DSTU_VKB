"""
Вариант 1. Определить число, образованное k старшими цифрами введенного с клавиатуры натурального числа.
Исходное число и значение k вводятся с клавиатуры. Пример: для числа 456771 и k: = 2 искомое число равно 45.
"""


def main() -> None:
    # Ввод исходных данных
    n: str = input("Введите натуральное число: ").strip()
    k: int = int(input("Введите k: "))

    result: int

    if k <= 0:
        result = 0
    elif k > len(n):
        result = int(n)  # Все цифры, если k больше длины числа
    else:
        result = int(n[:k])  # Первые k цифр

    print(f"Результат: {result}")


if __name__ == "__main__":
    main()
