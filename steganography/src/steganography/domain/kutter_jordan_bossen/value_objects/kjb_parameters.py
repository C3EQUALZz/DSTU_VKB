"""KjbParameters — настраиваемые параметры метода Куттера-Джордана-Боссена.

* ``lambda_factor`` — относительная сила встраивания (доля от яркости).
  Малые значения (~0.05) дают визуально неотличимый результат, но
  повышают вероятность ошибок извлечения; крупные (~0.3) — наоборот.
* ``seed`` — общий с приёмником seed PRNG для повторяемого выбора
  пикселей-носителей.
* ``neighbour_radius`` — радиус крестообразной окрестности соседей,
  по которым считается среднее синего канала при извлечении (1 = 4
  соседа сверху/снизу/слева/справа).
"""

from dataclasses import dataclass


@dataclass(frozen=True, slots=True)
class KjbParameters:
    """Параметры алгоритма Куттера-Джордана-Боссена."""

    lambda_factor: float
    seed: int
    neighbour_radius: int = 1
